static
bool
checkSimp (vector<DSimplex> simplices) {
// for each Simplex test every point not part of it
// to see if the point is contained in it's circumsphere
	Trace trc(1,"checkSimp");
	size_t i, j, pi, pj;

	trc.dprint("checking ",simplices.size()," Simplex");

	for (i=0; i<simplices.size(); i++) {
		for (j=0; j<simplices.size(); j++) {
			if (j == i) continue;
			for (pj=0; pj<simplices[j].size(); pj++) {
				for (pi=0; pi<simplices[i].size(); pi++) {
					if (simplices[i][pi] ==
							simplices[j][pj]) break;
				}
				if (pi <simplices[i].size())
					break;
				if (simplices[i].intersects(simplices[j][pj])) {
					trc.dprint("returning false: simplex ",j, " intersects with ",i);
					return false;
				}
			}
		}
	}
	return true;
}

static
bool
indices(int idx, vector<int> const& nitem, vector<int>& rval) {
// given an index (idx) into an n-dimensional array with "nitem"
// items in each coord, set the index of each coord in "rval"
	Trace trc(2,"indices ",idx);
	size_t n = nitem.size();
	size_t i;

	trc.dprintv(nitem,"number of items in each coord:");

	// initialize and check for idx out of range
	int maxidx = 1;
	for (i=0; i<n; i++) {
		rval[i] = 0;
		maxidx *= nitem[i];
	}
	if (idx >= maxidx) {
		trc.dprint("returning false: ",idx," out of the range 0-",maxidx-1);
		return false;
	}

	for (i=0; i<n; i++) {
		if (idx == 0)
			break;
		rval[i] = idx%nitem[i];
		idx /= nitem[i];
	}
	trc.dprintv(rval,"returning");
	return true;
}

void
biton(uint64_t& bv, int i) {
// i can range from 0 -> sizeof(uint64_t)-1
	uint64_t t{1};
	assert(i < 64);
	bv |= (t << i);
}

int
main(int argc, char** argv) {
// Usage:
//   delaunay n
	Trace trc(1,"main");
	size_t n = 2;   // default order
	ostringstream os;
	uint64_t mul{numeric_limits<uint64_t>::max()};
	Cube cube;

	try {
		trc.dprint("max unsigned long: ",mul," (",(double)mul,")");

		if (argc > 1) {
			n = atoi(argv[1]);
		}

		if (n > 7) {
			cerr << "maximum order is 7\n";
			exit(1);
		}

		cout << "delaunay triangulation of the "
			<< n << "D hypercube\n";

		vector<double> xmin(n, 0.0);
		vector<double> xmax(n, 1.0);
		cube = delaunay(xmin,xmax);

		// Convert each Simplex to a bitvector
		// the largest Simplex that will fit in a
		// 64-bit long int is:
		// n=7: 5040 Simplex, each with 8 points, 7 coord: 56 bits
		// n=8: 40320 Simplex, 9 points, 8 coord: 9*8 = 72 bits
		vector<uint64_t> bv(cube.size(), 0);
		for (size_t i=0; i<cube.size(); i++) {        // Simplex i
			assert(cube[i].size() == n+1);
			for (size_t j=0; j<cube[i].size(); j++) {  // Point j
				assert(cube[i][j].size() == n);
				for (size_t k=0; k<cube[i][j].size(); k++) {  // coord k
					if (is_equal(cube[i][j][k], 1.0, 8)) {
						// each bv is a (n,n+1) array of bits
						// each column is a Point, the n+1 Points
						// make a Simplex
						biton(bv[i], IJ(k,j,n));
					} else if (!is_equal(cube[i][j][k], 0.0, 8)) {
						cout << "Cube " << i << " Simplex " << j
							<< " Point " << k << " should be 0 or 1"
							<< " but is " << cube[i][j][k] << endl;
						exit(1);
					}
				}
			}
		}
		trc.dprintv(bv,"bitvector");

		// test proto
		Cube pr = proto(n);

		// compare with cube from delaunay
		for (size_t i=0; i<cube.size(); i++) {       // Simplex i
			for (size_t j=0; j<cube[i].size(); j++) { // Point j
				if (cube[i][j] != pr[i][j]) {
					cerr << "Simplex " << i << " Point " << j
						<< " do not match: " << cube[i][j] << " and "
						<< pr[i][j] << " (from proto) both have bv:\n";
					cerr << bitset<sizeof(uint64_t)*8>(bv[0]) << endl;
					exit(1);
				}
			}
		}
		
		// until ../picard.c is changed:
		trc.dprintv(cube,n,"D hypercube: ",cube.size()," Simplex");
		trc.dprintv(pr,"proto");
	} catch (runtime_error& s) {
		cerr << "caught exception: " << s << endl;
	}
}
