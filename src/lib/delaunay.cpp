static
double
circumsphere (vector<vector<double> > const& x, vector<double>& center) {
// Input:
//   x   n+1 n-vectors: n+1 points in nD space
// Returns:
//   radius
//   center  n-vector
	Trace trc(1,"circumsphere");
	size_t n = center.size();
	vector<double> A(n*n);
	vector<double> AF(n*n);
	vector<double> r(n);
	vector<double> c(n);
	vector<double> b(n);
	vector<int> ipiv(n);
	double rcond, ferr, berr;
	size_t i, j;
	double xx0;
	char equed[2];
	double rval = 0.0;
	ostringstream os;

	if (x.size() != n+1) {
		throw runtime_error(vastr(x.size(), " x points in ", n, "-dimensions"));
	}

	for (i=0; i<n+1; i++)
		assert(x[i].size() == n);

	trc.dprintv(x[0], "x[0]");

	// Compute the center by solving
	//    Ac = b
	// where c is the center, A_i: = x_{i+1} - x_0 (i=0,n-1)
	// and b_i = (x_{i+1}^t x_{i+1} - x_0^t x_0)/2 (i=0,n-1)
	blas_dot(n, &x[0][0], 1, &x[0][0], 1, xx0);
	for (i=0; i<n; i++) {
		for (j=0; j<n; j++) {
			A[IJ(i,j,n)] = x[i+1][j] - x[0][j];
		}
		double xx;
		blas_dot(n, &x[i+1][0], 1, &x[i+1][0], 1, xx);
		b[i] = 0.5*(xx - xx0);
		trc.dprintv(x[i+1], "x[",i+1,"]");
	}
	trc.dprintm(n,n,n,A,"A");
	trc.dprintv(b, "b");

	// solve Ac = b for the center c
	int info;
	int one{1};
	vector<double> work(4*n);
	vector<int> iwork(n);
	dgesvx_("E", "N", &n, &one, &A[0], &n, &AF[0],
			&n, &ipiv[0], equed, &r[0], &c[0], &b[0],
			&n, &center[0], &n, &rcond, &ferr, &berr, &work[0], &iwork[0], &info);
	if (info != 0) {
		throw runtime_error(vastr("singular matrix (dgesvx returned ", info,
			") rcond = ", rcond));
	}
	trc.dprint("sgesvx: rcond ", rcond);
	trc.dprintv(center, "center");
	// compute the radius squared - recycle "r"
	blas_copy (n, &x[0][0], 1, &r[0], 1);
	blas_axpy (n, -1.0, &center[0], 1, &r[0], 1);
	blas_dot(n, &r[0], 1, &r[0], 1, rval);
	trc.dprint("returning radius squared ",rval);
	return rval;
}

bool
intersectsSphere (Point const& pt,
		double radiusSq, vector<double> const& center) {
// determine if a point (n-vector of ints) is contained
// inside a sphere with center "center" and radius squared
	double t = radiusSq;
	double eps = sqrt(numeric_limits<double>::epsilon());
	// double eps = (numeric_limits<double>::epsilon());
	// double eps = 0.0;
	assert(pt.size() == center.size());
	// compute radiusSq - square(pt-center) one term at a time,
	// quit if the sum drops below eps
	for (size_t i=0; i<pt.size(); i++) {
		double ti = pt[i] - center[i];
		t -= ti*ti;
		if (t < eps) {
			return false;
		}
	}
	return true;
}

bool
DSimplex::
intersects (Point const& pt) const {
// determine if a point (n-vector of ints) is contained
// inside this DSimplex's circumsphere
	return intersectsSphere(pt, radiusSquared, center);
}


