static
DSimplex
initialSimplex (size_t n) {
	Trace trc(1,"initialSimplex");
	vector<double> center(n,1.0);
	size_t i, j;

	// give the initial Simplex an arbritrarily large
	// radius (squared)
	double radiusSq{0.0};
	for (i=0; i<n; i++) {
		int npts = 2;
		radiusSq += npts*npts;
	}
	radiusSq *= 100.0;
	trc.dprint("radius squared = ", radiusSq);

	// SVD of a random matrix to get an orthonormal U
	size_t nsq = n*n;
	vector<double> A(nsq);
	for (i=0; i<nsq; i++) {
		A[i] = flaps::xrand();
	}
	vector<double> u(nsq);
	vector<double> vt(nsq);
	vector<double> s(n);
	int info;
	// ftn_dgesvd_("A","A",n,n,&A[0],n,&s[0],&u[0],n,&vt[0],n);
	int lwork{-1};
	double dlwork;
	// workspace query
	dgesvd_("A", "A", &n, &n, &A[0], &n,
	 	&s[0], &u[0], &n, &vt[0], &n, &dlwork, &lwork, &info);
	lwork = dlwork;
	vector<double> work(lwork);
	dgesvd_("A", "A", &n, &n, &A[0], &n,
	 	&s[0], &u[0], &n, &vt[0], &n, &work[0], &lwork, &info);
	if (info != 0) {
		throw runtime_error(vastr("initial Simplex failed: dgesvd returned ",info));
	}

	double rad = sqrt(radiusSq);
	vector<double> jpt(n);
	vector<Point> pts;
	// the first n points are the columns of u scaled by rad
	// these vectors are orthonormal
	trc.dprintv(center,"center");
	trc.dprintv(u,"first n radii");
	for (j=0; j<n; j++) {
		for (i=0; i<n; i++) {
			jpt[i] = (rad*u[IJ(i,j,n)] + center[i]);
		}
		Point point(jpt,true);
		pts.push_back(point);
	}
	// last (n+1) pt: combination of -u
	vector<double> w(n, 1.0);
	vector<double> z(n);
	blas_sgemv ("n", n, n, 1.0, &u[0], n, &w[0], 1, 0.0, &z[0], 1);
	double znorm = blas_snrm2(n, &z[0], 1);
	double t = -rad/znorm;
	blas_scal(n, t, &z[0], 1);

	for (i=0; i<n; i++) {
		jpt[i] = (z[i] + center[i]);
	}
	pts.push_back(Point(jpt,true));

	DSimplex rval(pts);
	trc.dprint("initial Simplex",rval);
	if (trc() > 0) rval.plot(0, "initialSimplex.apf", false);
	assert(rval.size() == n+1);
	return rval;
}

